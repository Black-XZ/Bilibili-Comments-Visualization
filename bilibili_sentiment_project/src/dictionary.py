def clean_noise(text: str) -> str:
    """清洗噪声前缀"""
    for pattern in NOISE_PATTERNS:
        text = re.sub(pattern, '', text)
    return text.strip()


def has_meaningful_content(text: str) -> bool:
    """检查清洗后是否还有实质内容"""
    if not text or len(text.strip()) < 2:
        return False
    # 至少包含2个非停用词的有效字符
    stopwords = get_all_stopwords()
    meaningful_chars = [c for c in text if c not in stopwords and not c.isspace()]
    return len(meaningful_chars) >= 2


def detect_irony(text: str):
    """
    检测反讽，返回 (is_irony, sentiment, confidence)
    sentiment: 'positive' | 'negative' | 'neutral'
    """
    for pattern, sentiment, conf in IRONY_PATTERNS:
        if re.search(pattern, text):
            return True, sentiment, conf
    return False, None, 0


def calc_game_term_ratio(text: str) -> float:
    """计算文本中游戏术语的覆盖比例"""
    if not text:
        return 0.0
    game_found = [t for t in GAME_SENTIMENT_DICT.keys() if t in text]
    if not game_found:
        return 0.0
    covered = sum(len(t) for t in game_found)
    return min(covered / len(text), 1.0)


def apply_intensity_modifiers(text: str, score: float) -> float:
    """应用情感强度修饰符"""
    intensity = 1.0

    # 词语强度
    for mod, factor in INTENSITY_MODIFIERS.items():
        if mod in text:
            intensity = max(intensity, factor)

    # 标点强度
    for punct, factor in PUNCTUATION_INTENSITY.items():
        if punct in text:
            intensity *= factor

    # 感叹号数量
    exclaim = text.count('！') + text.count('!')
    if exclaim >= 4:
        intensity *= 1.3
    elif exclaim >= 2:
        intensity *= 1.15

    # 问号数量（质疑/惊讶）
    question = text.count('？') + text.count('?')
    if question >= 2:
        intensity *= 1.1

    # 远离中性
    if score > 0.5:
        score = min(1.0, 0.5 + (score - 0.5) * intensity)
    elif score < 0.5:
        score = max(0.0, 0.5 - (0.5 - score) * intensity)

    return score


def score_to_label(score: float) -> str:
    """将0-1分数转为情感标签"""
    if score >= 0.6:
        return '正面'
    elif score <= 0.4:
        return '负面'
    return '中性'


